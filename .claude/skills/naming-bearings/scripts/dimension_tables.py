# ---- bore code -> bore diameter d (mm) ----
def bore_from_code(code):
    """code = 2-digit bore code string for metric bearings."""
    m = {'00':10,'01':12,'02':15,'03':17}
    if code in m: return m[code]
    try:
        n=int(code)
    except: return None
    if n>=4: return n*5
    return None  # single-digit handled elsewhere

